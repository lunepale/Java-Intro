package section3;

public class RelationalEx {
    public static void main(String[] args) {
        var a = 2;
        var b = 3;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);
    }
}
