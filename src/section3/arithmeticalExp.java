package section3;

public class arithmeticalExp {
    public static void main(String[] args) {
        var a = 5.;
        var b = 3.;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
