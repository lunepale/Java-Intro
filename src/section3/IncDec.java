package section3;

public class IncDec {
    public static void main(String[] args) {
        var a=0;
        System.out.println(a);
        a=5;
        System.out.println(a);
        a = a+2;
        System.out.println(a);
        a ++;  //a + 1 -> a += 1
        System.out.println(a);
        a --;  //a - 1 -> a -= 1
        System.out.println(a);
    }
}
