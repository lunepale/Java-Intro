package section5;

public class DisplayEvenNumbersFrom0to50 {
    public static void main(String[] args) {
        //read
        var from = 1;
        var to = 50;
        var step = 1;


        //process
        for (int i = from; i <= to; i += step) {
            if (i % 2 == 0) {
                System.out.print(i+" ");
            }

}


















    }
}
